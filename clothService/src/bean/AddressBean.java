package bean;

public class AddressBean {
	private String address_id;
	private String contact_name;
	private String contact_phone;
	private String creator_phone;
	private String address_total;
	private String address_detail;
	private String userid;
	public String getAddress_id() {
		return address_id;
	}
	public void setAddress_id(String address_id) {
		this.address_id = address_id;
	}
	public String getContact_name() {
		return contact_name;
	}
	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}
	public String getContact_phone() {
		return contact_phone;
	}
	public void setContact_phone(String contact_phone) {
		this.contact_phone = contact_phone;
	}
	public String getCreator_phone() {
		return creator_phone;
	}
	public void setCreator_phone(String creator_phone) {
		this.creator_phone = creator_phone;
	}
	public String getAddress_total() {
		return address_total;
	}
	public void setAddress_total(String address_total) {
		this.address_total = address_total;
	}
	public String getAddress_detail() {
		return address_detail;
	}
	public void setAddress_detail(String address_detail) {
		this.address_detail = address_detail;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}


}
